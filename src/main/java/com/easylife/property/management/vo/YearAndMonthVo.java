package com.easylife.property.management.vo;

public class YearAndMonthVo {

	private String yearAndMonthVo;
	private Integer year;
	private Integer month;
	public String getYearAndMonthVo() {
		return yearAndMonthVo;
	}
	public void setYearAndMonthVo(String yearAndMonthVo) {
		this.yearAndMonthVo = yearAndMonthVo;
	}
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public Integer getMonth() {
		return month;
	}
	public void setMonth(Integer month) {
		this.month = month;
	}
	
}
